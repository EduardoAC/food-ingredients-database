#!/usr/bin/env node
import { promises as fs } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import prettier from 'prettier'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

const projectRoot = path.resolve(__dirname, '..')
const databaseDir = path.resolve(projectRoot, 'database', 'fdc')
const indexPath = path.join(databaseDir, 'index.json')
const outputPath = path.resolve(projectRoot, 'src', 'local', 'bundledShards.ts')

function formatImportName(shardName) {
  return `shard${shardName.replace(/[^a-zA-Z0-9]/g, '_')}`
}

async function main() {
  const indexRaw = await fs.readFile(indexPath, 'utf8')
  const indexData = JSON.parse(indexRaw)
  const sortedShards = [...indexData.shards].sort((left, right) =>
    left.shard.localeCompare(right.shard)
  )
  const sortedIndex = {
    ...indexData,
    shards: sortedShards
  }
  const imports = []
  const entries = []

  for (const entry of sortedShards) {
    const importName = formatImportName(entry.shard)
    const shardRelativePath = path
      .relative(
        path.dirname(outputPath),
        path.resolve(databaseDir, 'shards', entry.shard)
      )
      .replace(/\\/g, '/')
    imports.push(`import ${importName} from '${shardRelativePath}';`)
    entries.push(`  '${entry.shard}': ${importName} as StoredFoodItem[],`)
  }

  const fileContents = `/**
 * This file is generated by scripts/generate-bundled-shards.mjs.
 * Do not edit manually.
 */
import type { StoredFoodItem } from '../sync/adapters/jsonShardedDatabaseAdapter'

${imports.join('\n')}

export const bundledShards: Record<string, StoredFoodItem[]> = {
${entries.join('\n')}
}

export const bundledIndex = ${JSON.stringify(sortedIndex, null, 2)} as const
`
  const prettierConfig = (await prettier.resolveConfig(outputPath)) ?? {}
  const formattedFileContents = await prettier.format(fileContents, {
    ...prettierConfig,
    filepath: outputPath
  })

  await fs.writeFile(outputPath, formattedFileContents, 'utf8')
  console.log(`[generate-bundled-shards] Wrote ${outputPath}`)
}

main().catch((error) => {
  console.error('[generate-bundled-shards] Failed to generate shard map', error)
  process.exitCode = 1
})
